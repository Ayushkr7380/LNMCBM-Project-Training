#<-----------------Comparision operator-------------------->

#equalto operator
def equalto(x,y):
    return x==y
n1 = int(input('Enter 1st number : '),10)
n2 = int(input('Enter 2nd number : '),10)
print(equalto(n1,n2)) #false

#notequalto operator
def notequalto(x,y):
    return x!=y
n1 = int(input('Enter 1st number : '),10)
n2 = int(input('Enter 2nd number : '),10)
print(notequalto(n1,n2)) #false

#greaterthanequalto operator
def greaterthanequalto(x,y):
    return x>=y
n1 = int(input('Enter 1st number : '),10)
n2 = int(input('Enter 2nd number : '),10)
print(greaterthanequalto(n1,n2)) #True

#lessthanequalto operator
def lessthanequalto(x,y):
    return x<=y
n1 = int(input('Enter 1st number : '),10)
n2 = int(input('Enter 2nd number : '),10)
print(lessthanequalto(n1,n2)) #true

#lessthan operator
def lessthan(x,y):
    return x<y
n1 = int(input('Enter 1st number : '),10)
n2 = int(input('Enter 2nd number : '),10)
print(lessthan(n1,n2)) #False

#greaterthan operator
def greaterthan(x,y):
    return x>y
n1 = int(input('Enter 1st number : '),10)
n2 = int(input('Enter 2nd number : '),10)
