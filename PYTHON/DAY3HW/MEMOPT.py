#IN and NOTIN operator
def INANDNOTIN(x):
    x = ['lotus' , 'rose']
    return x
a = INANDNOTIN([])
print('Is value Present?' , 'lotus' in a) #Is value Present? True
print('Is value not present?', 'ayush' not in a) #Is value not present? True 

